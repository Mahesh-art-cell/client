import "./rightBar.scss";
import { useEffect, useState } from "react";
import { makeRequest } from "../../axios"; // ✅ Correct import

const RightBar = () => {
  const [suggestions, setSuggestions] = useState([]);

  // ✅ Fetch User Suggestions
  useEffect(() => {
    const fetchSuggestions = async () => {
      try {
        const res = await makeRequest.get("/relationships/suggestions"); // ✅ Use makeRequest
        console.log("✅ Suggestions Data:", res.data);
        setSuggestions(res.data);
      } catch (err) {
        console.error("❌ Error fetching suggestions:", err);
      }
    };
    fetchSuggestions();
  }, []);

  // ✅ Handle Follow User
  const handleFollow = async (userId) => {
    try {
      await makeRequest.post("/relationships", {
        followedUserId: userId,
      });
      // ✅ Remove followed user from suggestions
      setSuggestions((prev) => prev.filter((user) => user.id !== userId));
    } catch (err) {
      console.error("❌ Error following user:", err);
    }
  };

  // ✅ Handle Dismiss User
  const handleDismiss = (userId) => {
    setSuggestions((prev) => prev.filter((user) => user.id !== userId)); // Remove user from suggestions
  };

  return (
    <div className="rightBar">
      <div className="container">
        {/* ✅ Suggestions Section */}
        <div className="item">
          <span>Suggestions For You</span>
          {suggestions.length > 0 ? (
            suggestions.map((user) => (
              <div className="user" key={user.id}>
                <div className="userInfo">
                  <img
                    src={user.profilePic || "https://via.placeholder.com/50"}
                    alt={user.username}
                  />
                  <span>{user.username}</span>
                </div>
                <div className="buttons">
                  <button onClick={() => handleFollow(user.id)}>Follow</button>
                  <button onClick={() => handleDismiss(user.id)}>Dismiss</button>
                </div>
              </div>
            ))
          ) : (
            <p>No suggestions available!</p>
          )}
        </div>

        {/* ✅ Latest Activities Section */}
        <div className="item">
          <span>Latest Activities</span>
          <div className="user">
            <div className="userInfo">
              <img src="https://via.placeholder.com/50" alt="activity" />
              <p>
                <span>John Doe</span> changed their cover picture
              </p>
            </div>
            <span>1 min ago</span>
          </div>
        </div>

        {/* ✅ Online Friends Section */}
        <div className="item">
          <span>Online Friends</span>
          <div className="user">
            <div className="userInfo">
              <img src="https://via.placeholder.com/50" alt="friend" />
              <div className="online" />
              <span>Jane Doe</span>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default RightBar;
